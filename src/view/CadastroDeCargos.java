    private void jTCargosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTCargosKeyReleased
        if(jTCargos.getSelectedRow() != -1) {
            txtNomeCargo.setText(jTCargos.getValueAt(jTCargos.getSelectedRow(), 0).toString());
        }
    }//GEN-LAST:event_jTCargosKeyReleased

