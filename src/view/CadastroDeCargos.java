    // Para escrever na jTable.
    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTCargos.getModel();
        modelo.setNumRows(0); // Evita a dublicação dos dados na hora da inserção.
        CargoDAO cargoDao = new CargoDAO();

        for (Cargo cargo : cargoDao.read()) {

            modelo.addRow(new Object[]{
                cargo.getNomeCargo()
            });
        }
    }


